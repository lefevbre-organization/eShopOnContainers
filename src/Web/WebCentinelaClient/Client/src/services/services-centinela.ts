interface CentinelaResponse {
  errors: any;
  infos: any;
}

export interface CentUser {
  id: number | string | null;
  idNavision: string;
  name: string;
  evaluations: Evaluation[];
  extraElements: any;
}

export interface CentUserResponse extends CentinelaResponse {
  data: CentUser;
}

export interface CentInstance {
  conceptId: number;
  conceptObjectId: number;
  folderId: number;
  evaluated: boolean;
  title: string;
  description: string;
  author: string;
  isArchived: boolean;
  creationDate: string;
  modificationDate: string;
}

export interface CentInstanceResponse extends CentinelaResponse {
  data: CentInstance[];
}

export interface Evaluation {
  evaluationId: number;
  name: string;
  productId: string;
  productName: string;
  clientId: number;
  clientName: string;
  modified: string;
  progress: number;
  status: number;
  risk: boolean;
  normChange: boolean;
  canManage: boolean;
  canModify: boolean;
}

export interface EvaluationResponse extends CentinelaResponse {
  data: Evaluation | Evaluation[];
}

export interface Concept {
  conceptId: number;
  conceptObjectId: number;
  evaluated: boolean;
  title: string;
  description: string | null;
  published: boolean;
  allowPublicShare: boolean;
  publicShared: boolean;
  publicUrl: string;
  isFront: boolean;
  hasProcessOps: boolean;
  hidden: boolean;
  singleInstance: boolean;
  modified: string;
  iD_Version0: number | null;
}
export interface ConceptResponse extends CentinelaResponse {
  data: Concept[];
}

export interface TreeNode {
  folderId: number;
  parentId: number | null;
  name: string;
  description: string | null;
  published: boolean;
  conceptObjectId: number;
  evaluated: boolean;
  concepts: Concept[];
  children: TreeNode[];
  totalConcepts: number;
  isFront: boolean;
  hidden: boolean;
  iD_Version0: number;
  level: number;
}
export interface TreeNodeResponse extends CentinelaResponse {
  data: TreeNode[];
}

